/********************************************************************/
/*								    */
/*                   Реализация инструкции Exclude                  */
/*								    */
/*     EXCLUDE <Спецификация_1> <Спецификация_2>                    */
/*          где <Спецификация_i> - <Объект>[.<Звено>[.<Тело>]]      */

  int  RSS_Module_Cross::cExclude(char *cmd)

{ 
#define   _PARS_MAX   4
                    char *pars[_PARS_MAX] ;
              RSS_Object *object ;
  RSS_Feature_Cross_Mark  spec[2] ;
  RSS_Feature_Cross_Mark *mark_1 ;
                    char *next ;
                    char *end ;
                    char  text[1024] ;
                     int  n ;
                     int  i ;

#define   OBJECTS       this->kernel->kernel_objects 
#define   OBJECTS_CNT   this->kernel->kernel_objects_cnt 

/*------------------------------------------------ Разбор параметров */

    for(i=0 ; i<_PARS_MAX ; i++)  pars[i]=NULL ;

    for(end=cmd, i=0 ; i<_PARS_MAX ; end++, i++) {
      
                pars[i]=end ;
                   end =strchr(pars[i], ' ') ;
                if(end==NULL)  break ;
                  *end=0 ;
                                                 }
/*-------------------------------------- Контроль наличия спецификаций */

    if(*pars[0]== 0) {                                                /* Если имя не задано... */
          SEND_ERROR("Должна быть задана хотябы одна спецификация\n"
                     "Например: EXCLUDE robot.link1") ;
                                     return(-1) ;
                     }

    if(pars[1]==NULL)  pars[1]="*" ;

/*---------------------------------------------- Разбор спецификаций */

     for(n=0 ; n<2 ; n++) {
/*- - - - - - - - - - - - - - - -  Разбор спецификации на компоненты */
       do {                                                         /* BLOCK.1 */
               end=strchr(pars[n], '.') ;
            if(end!=NULL)  *end=0 ;

                             strcpy(spec[n].object, pars[n]) ;

            if(end==NULL) {  strcpy(spec[n].link, "*") ;
                             strcpy(spec[n].body, "*") ;
                                        break ;            }

              next=end+1 ;
               end=strchr(next, '.') ;
            if(end!=NULL)  *end=0 ;

                             strcpy(spec[n].link, next) ;

            if(end==NULL) {  strcpy(spec[n].body, "*") ;
                                        break ;            }

                             strcpy(spec[n].body, end+1) ;
          } while(0) ;                                              /* BLOCK.1 */
/*- - - - - - - - - - - - - - - - - -  Проверка корректности объекта */
         if(spec[n].object[0]=='*')  continue ;

       for(i=0 ; i<OBJECTS_CNT ; i++)                               /* Ищем объект по имени */
         if(!stricmp(OBJECTS[i]->Name, spec[n].object)) {
                                  object=OBJECTS[i] ;
                                             break ;
                                                        }

         if(i==OBJECTS_CNT) {                                       /* Если имя не найдено... */
                  sprintf(text, "Объекта с именем '%s' "
                                 "НЕ существует", spec[n].object) ;
               SEND_ERROR(text) ;
                               return(-1) ;
                            }
/*- - - - - - - - - - - - - - - - - - -  Проверка корректности звена */
         if(spec[n].link[0]=='*')  continue ;

       for(i=0 ; i<object->Morphology_cnt ; i++)
         if(!stricmp(object->Morphology[i].link, spec[n].link))  break ;

         if(i==object->Morphology_cnt) {                            /* Если имя не найдено... */
                  sprintf(text, "Объекта '%s' не имеет "
                                "звена с именем '%s'", 
                                   spec[n].object, spec[n].link) ;
               SEND_ERROR(text) ;
                      return(-1) ;
                                       }
/*- - - - - - - - - - - - - - - - - - - - Проверка корректности тела */
       if(spec[n].body[0]=='*')  continue ;

       for(i=0 ; i<object->Morphology_cnt ; i++)
         if(!stricmp(object->Morphology[i].link, spec[n].link) &&
            !stricmp(object->Morphology[i].body, spec[n].body)   )  break ;

         if(i==object->Morphology_cnt) {                            /* Если имя не найдено... */
                  sprintf(text, "Звено '%s' объекта '%s' не имеет "
                                "тела с именем '%s'", 
                                 spec[n].link, spec[n].object, spec[n].body) ;
               SEND_ERROR(text) ;
                      return(-1) ;
                                       }
/*- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -*/
                          }
/*------------------------------------------------- Регистрация пары */

          mark_1=RSS_Feature_Cross::iSetMark(&spec[0], &spec[1]) ;  /* Регистрируем пару */
       if(mark_1==NULL) {
                 SEND_ERROR("Не удалось зарегестрировать связь") ;
                                        return(FALSE) ;
                        }
/*-------------------------------------------------------------------*/

#undef    OBJECTS
#undef    OBJECTS_CNT

   return(0) ;
}


